-- ─────────────────────────────────────────────────────────────────────────────
-- 1) إلغاء أي جدولة سابقة بنفس الأسماء (للسماح بإعادة التشغيل)
-- ─────────────────────────────────────────────────────────────────────────────
DO $$
BEGIN
  PERFORM cron.unschedule(jobid) FROM cron.job
    WHERE jobname IN (
      'daily-expire-offers',
      'daily-expire-boosts',
      'hourly-appointment-reminders'
    );
EXCEPTION WHEN OTHERS THEN
  NULL; -- تجاهل لو الجداول مش موجودة بعد
END $$;

-- ─────────────────────────────────────────────────────────────────────────────
-- 2) جدولة expire_offers — يومياً الساعة 3 صباحاً (بتوقيت UTC)
--    التوقيت: 3:00 UTC = 6:00 صباحاً بتوقيت دمشق (UTC+3)
-- ─────────────────────────────────────────────────────────────────────────────
SELECT cron.schedule(
  'daily-expire-offers',
  '0 3 * * *',  -- كل يوم الساعة 03:00 UTC
  $$ SELECT expire_offers(); $$
);

-- ─────────────────────────────────────────────────────────────────────────────
-- 3) جدولة expire_offer_boosts — يومياً الساعة 3:05 صباحاً
-- ─────────────────────────────────────────────────────────────────────────────
SELECT cron.schedule(
  'daily-expire-boosts',
  '5 3 * * *',  -- كل يوم 03:05 UTC
  $$ SELECT expire_offer_boosts(); $$
);

-- ─────────────────────────────────────────────────────────────────────────────
-- 4) جدولة send_appointment_reminders — كل ساعة
-- ─────────────────────────────────────────────────────────────────────────────
SELECT cron.schedule(
  'hourly-appointment-reminders',
  '0 * * * *',  -- بداية كل ساعة
  $$ SELECT send_appointment_reminders(); $$
);

